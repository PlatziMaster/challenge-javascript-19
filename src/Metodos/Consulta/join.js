//el metodo join() une todos los elementos de una matriz(array o un objeto similar a una matriz) en una cadena y devuelve esta cadena

const phrase = ['Nunca', 'Pares', 'De', 'Aprender'];

console.log(phrase.join()); //Nunca,Pares,De,Aprender
console.log(phrase.join('')); //NuncaParesDeAprender
console.log(phrase.join('-')); //Nunca-Pares-De-Aprender
